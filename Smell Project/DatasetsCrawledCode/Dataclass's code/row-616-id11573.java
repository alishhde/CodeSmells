 public class Application {
 private String name;
 private Map<String, String> inputs;


 public Application() {
        }


 public String getName() {
 return name;
        }


 public void setName(String name) {
 this.name = name;
        }


 public Map<String, String> getInputs() {
 return inputs;
        }


 public void setInputs(Map<String, String> inputs) {
 this.inputs = inputs;
        }
    }